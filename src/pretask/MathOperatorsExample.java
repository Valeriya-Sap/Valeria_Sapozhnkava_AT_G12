package pretask;

public class MathOperatorsExample {
    public static void main(String[] arg) {
        int a = 32;
        int b = 10;
        int sum = a + b;
        System.out.println("a+b=" + sum);
        int ded = a - b;
        System.out.println("a-b=" + ded);
        int mult = a * b;
        System.out.println("a*b=" + mult);
        double div = (double) a / b;
        System.out.println("a/b=" + div);
    }
}
